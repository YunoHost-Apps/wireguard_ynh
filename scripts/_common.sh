    pkg_headers="raspberrypi-kernel-headers|linux-headers-generic|linux-headers-virtual|linux-headers-$(uname -r)"
else
    pkg_headers=""
fi

# dependencies used by the app
pkg_dependencies="$pkg_headers wireguard-dkms wireguard"

